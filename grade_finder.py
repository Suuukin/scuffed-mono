score = int(input('What percent did you get (0-100)?'))
if score >= 86:
    print('The percent', score, 'is an A')
elif score >= 73:
    print('The percent', score, 'is an B')
elif score >= 67:
    print('The percent', score, 'is an C+')
elif score >= 60:
    print('The percent', score, 'is an C')
elif score >= 50:
    print('The percent', score, 'is an C-')
elif score >= 0:
    print('The percent', score, 'is an F')
